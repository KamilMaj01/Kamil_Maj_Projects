char* Recalloc(char *text){
	size_t size = strlen(text);
	char* New_Text = calloc(size + 2, sizeof(char));
	if(New_Text == NULL){
			printf("Error: Problem Z alokacja\n");
		}
	for(size_t i = 0; i <=size; i++){
		New_Text[i] = text[i];
	}
	free(text);
	return New_Text;
}


